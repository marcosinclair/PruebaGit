package Marcos;

/**
 * Esta clase resta dos números, uno mayor y otro menor
 * 
 * @version 1.0
 * @author Marcos
 */
public class Resta {
    
    public Resta ()
    {
        
    }
    
    /**
     * 
     * @param numeroMayor el número mayor al que le restaremos el otro
     * @param numeroMenor el número que se resta
     * @return int el resultado de la resta
     */
    public int restar(int numeroMayor, int numeroMenor)
    {
        return numeroMayor - numeroMenor;
    }
}
