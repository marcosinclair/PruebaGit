public class Suma {
    
    
    public Suma () {
    }
    
    /**
     * Devuelve la suma de un numero y el campo de la clase.
     * @param entero int a sumar.
     * @param numero int a sumar.
     * @return int resultado de la suma.
     */
    public int sumar (int entero,int numero) {
        return (entero + numero);
    }
    
}
